def day_of_the_week(dayNumber):
    if dayNumber == 0 :
        return sunday   
    elif dayNumber == 1 :
        return monday
    elif dayNumber == 2 :
        return tuesday
    elif dayNumber == 3 :
        return  wednesday
    elif dayNumber == 4 :
        return  thursday 
    elif dayNumber == 5 :
        return friday
    elif dayNumber == 6 :
        return saturday